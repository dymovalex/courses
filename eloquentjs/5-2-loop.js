function loop(arg, flow, renew, body) {
	if(!flow(arg)) {
		return 'false statement';
	} else {
		body(arg);
		loop(renew(arg), flow, renew, body);
	}
	
}
/*Напишите функцию высшего порядка loop, которая представляет собой аналог оператора цикла for.
Она принимает значение, функцию условия, функцию обновления и функцию тела. На каждой итерации сначала выполняется
функция условия для текущего значения цикла. Если эта функция возвращает
false, то выполнение цикла прекращается. Затем вызывается функция тела,
которой передается текущее значение цикла. Наконец, вызывается функция
обновления для создания нового значения, и цикл запускается сначала.
При определении функции вы можете использовать обычный цикл для
перебора значений*/

console.log(loop(2, a => a <= 12, a => a += 2, a => console.log(`I'm alive! Count: ${a}`)));